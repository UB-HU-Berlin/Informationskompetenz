Glossar.init = function () {
	$(".glossar")
		.click(this.giveExp)
		.css({
			"cursor":"pointer",
			"text-decoration":"underline"
		})
		.prepend("<span class='fa fa-info-circle'>&nbsp;</span>")
	
}

Glossar.giveExp = function ( evt ) {

	var obj = evt.currentTarget;
	var key = ( $(obj).attr("class").split(" ")[1] );
	var string = Glossar.content[key]; 
	Glossar.putExpPopup( key, string, obj );
}

Glossar.putExpPopup = function ( key, string, obj ) {
	
	
	$("#expPopup").remove();
	$("body").append("<div id='popup-wrapper'></div>");
	
	$("#popup-wrapper")
		.append("<div id='expPopup'><div>")
		.css({
			"position":"absolute",
			"top":0,
			"left":0,
			"width":"100%",
			"height":"100%",
			"background-color":"rgba(0,0,0,.3)",	
			"z-index":999
		})
		.click( function () {
			$(this).remove();
		});
	
	$("#expPopup")
		.html("<h3>"+key+"</h3><p>"+string+"</p>")
		.css({
			"max-width":"25em",
			"min-height":"1em",
			"background-color":"white",
			"position":"absolute",
			"padding":"1em 2em 2em 2em",
			"left": $(obj).position().left + 100,
			"top": $(obj).position().top - 30
		})
		.append("<span class='fa fa-times' id='close-btn'></span>");
		
	$("<div class='underline'></div>").insertAfter("#expPopup h3");
	$("div.underline").css({
		"height":"2px",
		"width":"100%",
		"background-color":"black",
		"margin-top":"-1em"
	});
		
		$("#close-btn")
			.css({
				"position":"absolute",
				"right":"0",
				"top":"0",
				"cursor":"pointer",
				"font-size":"1.25em"
			})
			.click( function () { 
				$( "#popup-wrapper" ).remove();
			} );
}